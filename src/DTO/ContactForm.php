namespace App\DTO;

class ContactForm
{
    private $name;
    private $email;
    private $text;

    public function __construct(string $name = null, string $email = null, string $text = null)
    {
        $this->name = $name;
        $this->email = $email;
        $this->text = $text;
    }

    public function getName(): ?string
    {
        return $this->name;
    }

    public function setName(string $name): void
    {
        $this->name = $name;
    }

    public function getEmail(): ?string
    {
        return $this->email;
    }

    public function setEmail(string $email): void
    {
        $this->email = $email;
    }

    public function getText(): ?string
    {
        return $this->text;
    }

    public function setText(string $text): void
    {
        $this->text = $text;
    }
}
